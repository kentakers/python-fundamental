def main():
    # membuat list
    li = [100, 200, 300, 400, 500]

    # menampilkan elemen didalam list
    print("Menampilkan seluruh elemen didalam list = ", li)

    print("\nMenampilkan index positif")
    print(li[0])    # menampilkan elemen pada index ke 0
    print(li[1])    # menampilkan elemen pada index ke 1
    print(li[2])    # menampilkan elemen pada index ke 2
    print(li[3])    # menampilkan elemen pada index ke 3
    print(li[4])    # menampilkan elemen pada index ke 4

    print("\nMenampilkan index negatif")
    print(li[-1])    # menampilkan elemen pada index ke 0
    print(li[-2])    # menampilkan elemen pada index ke 1
    print(li[-3])    # menampilkan elemen pada index ke 2
    print(li[-4])    # menampilkan elemen pada index ke 3
    print(li[-5])    # menampilkan elemen pada index ke 4


if __name__ == '__main__':
    main()
